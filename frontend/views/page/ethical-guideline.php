<h4>Procedures and Penalties</h4>

<p>The assessment of appropriate penalties and notification will follow the guidelines set by the Journal&rsquo;s Editors as summarised below.<br />
<br />
The respective Editorial Boards are the primary committee in detecting plagiarism in the submitted manuscripts. Apart from that, the complainants will bring cases of suspected author(s) misconduct to the attention of the Managing Editor. The Managing Editor has to ensure that the following documentation is provided:

<ol style="list-style-type:lower-alpha;margin-left:40px">
<li>A written description of the alleged misconduct;&nbsp;</li>
<li>Title of the manuscript; and</li>
<li>A full list of the author(s)&rsquo; names.</li>
</ol>

<p>An alleged plagiarised manuscript should consist of:</p>

<ol style="list-style-type:lower-alpha;margin-left:40px">
<li>Title; and</li>
<li>List of the author(s)&rsquo; names and publication in which the manuscript appeared.</li>
</ol>

<p>&nbsp;</p>

<p>For the use of ideas, the content should consist of:</p>
<ol style="list-style-type:lower-alpha;margin-left:40px">
<li>Title of the idea;</li>
<li>A full list of creators and date of creation;</li>
<li>Copies of both manuscripts; and</li>
<li>Full name and address of the complainant.&nbsp;</li>
</ol>

<p>&nbsp;</p>

<p>The Managing Editor, after noticing a suspicion of plagiarism, will review all evidence and make a preliminary judgment regarding the claim. As part of the Managing Editor&#39;s deliberation, it is required that the author(s) be contacted and provided with an opportunity to rebut the charge. Lastly, if the Managing Editor finds sufficient evidence for justification of a charge of plagiarism, the Managing Editor shall appoint an ad hoc committee to make a determination of the charge. This committee should consist of, at a minimum, the Managing Editor and at least three other editorial team members.</p>

<p>&nbsp;</p>

<p>The ad hoc committee shall at first contact the author(s) in writing and ask for a response to the charge. Based on the response, the ad hoc committee may obtain additional information, which may include a review of the manuscript in question by experts to help determine the level of plagiarism. This committee should also determine whether the charge is to be upheld and also the sanction, which is to be imposed against the author(s). Sanctions would typically include a ban from submission to the journal for a period of time. The committee has the sole responsibility and authority to determine the sanction. Sanctions may be applied unevenly in the case of multiple authors.<br />
<br />
Once the finding and the sanctions are determined, the Managing Editor will communicate the results in writing to the author(s). If the charge is not upheld, the process ends, and no further actions are taken. In particular, the results are only revealed to those who are already involved in the process.<br />
<br />
Given the serious nature of a charge of plagiarism, it is required that strict confidentiality be maintained throughout the process. The charge of plagiarism, supporting materials and outcome will only to be made known to those persons who are involved in the review process.<br />
<br />
If plagiarism has been identified, and after all appeals are exhausted, the ad hoc committee will determine the appropriate steps to ensure that it does not occur again. If the plagiarised article has appeared in printing, author(s) have to notify the readership. These steps may include notification to the employer(s) of the author(s).&nbsp;</p>

