<h4>Plagiarism</h4>

<p>Plagiarism is the copying of ideas, text, data and other creative work (e.g. tables, figures, and graphs) and presenting it as original research without proper citation. Therefore, to avoid plagiarism, the author(s) need to obtain permission to reuse previously published work (even if properly cited) from the holder of the copyright (which is typically not the author).<br />
<br />
It is essential that the author(s) inform editors and reviewers when any portion of an article is based heavily on previous work, even if this work has been written by one or more of the author(s) of the article. It is the responsibility of the author(s) not only to cite the previous work, including his or her own but also to indicate the extent to which an article depends on this work.<br />
<br />
According to these principles, the author(s) should recognise the following guidelines:</p>

<ol style="list-style-type:lower-alpha;margin-left:40px">
<li>Plagiarism covers the use of ideas that have been presented in prior work, regardless of whether the ideas are expressed using the same words, tables or graphics.</li>

<li>Word-for-word copying of the work by others must be clearly identified.&nbsp;</li>

<li>More extensive word-for-word copying of one&rsquo;s own work is permitted (with permission from the holder of any copyright), but this must be clearly indicated in the article.&nbsp;</li>

<li>Proper attribution of an idea is required even if a journal operates with the double-blind review.&nbsp;</li>

<li>The first article in which a creative contribution occurs (text, ideas, analysis) gets the credit for the contribution, even if it has not yet been accepted for publication. Subsequent articles (by the same or different author(s)) are expected to cite the first article (even if it is under review).</li>

<li>The use and reuse of empirical data follow the same principles as other types of research, although some issues are unique to the nature of data as opposed to the ideas expressed in text and mathematics.&nbsp;</li>

<li>The primary goal of this policy is transparent so that the editorial staff will understand what is new and original, and the degree to which the article is drawing on the work of others or the author(s). If unsure how to properly give credit to the work that is presented elsewhere (such as a parallel publication which is also under review or a conference proceeding), the best and preferred strategy is to describe the situation in a cover letter to the editor.</li>

</ol>

