<h3>1. Ethical Guideline for Authors</h3>
<br />

<p>JEB &nbsp;follows the&nbsp;<a href="https://publicationethics.org/">Committee on Publication Ethics</a> (COPE)&#39;s Code of Conduct for <a href="https://publicationethics.org/files/International%20standards_authors_for%20website_11_Nov_2011.pdf">International Standard for Authors</a></p>

<p>&nbsp;</p>
<h4>In Summary</h4>


<ol style="list-style-type:lower-alpha;margin-left:40px">
	<li>The research being reported should have been conducted in an ethical and responsible manner and should comply with all relevant legislation (including plagiarism).</li>
	<li>Author(s) should present their results clearly, honestly, and without fabrication, falsification or inappropriate data manipulation.</li>
	<li>Author(s) should strive to describe their methods clearly and unambiguously so that others can confirm their findings.</li>
	<li>Author(s) should adhere to publication requirements that submitted work is original, is not plagiarised, and has not been published elsewhere.</li>
	<li>Author(s) should take collective responsibility for submitted and published work.</li>
	<li>The authorship of research publications should accurately reflect the individuals&rsquo; contributions to the work, and it&rsquo;s reporting.</li>
	<li>Funding sources and relevant conflicts of interest should be disclosed.</li>
</ol>

<p>Source: COPE (<a href="https://publicationethics.org/files/International%20standards_authors_for%20website_11_Nov_2011.pdf">International Standard for </a><a href="https://publicationethics.org/files/International%20standards_authors_for%20website_11_Nov_2011.pdf">Authors</a>)</p>

