<p>&nbsp;</p>

<h4>In Summary</h4>

<ol style="list-style-type:lower-alpha;margin-left:40px">
<li>The reviewer who has a of interest, not capable the assigned manuscript and/or cannot meet the deadline for review completion should immediately notify the editor and recuse himself or herself from the process of reviewing the manuscript.</li>

<li>The reviewer should . The manuscript should not be disclosed to or discussed with others except as by the editor.</li>

<li>The reviewer should not use for his or her research any part of any data or work reported in submitted and as yet unpublished articles.</li>

<li>The reviewer should immediately notify the editor of any similarities between the manuscript under review and another article either published or under consideration by another journal.</li>

</ol>

