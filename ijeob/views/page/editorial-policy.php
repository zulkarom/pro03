			<br />
			<p>JEB follows the&nbsp;<a href="https://publicationethics.org/">Committee on Publication Ethics</a>&nbsp;&nbsp;(COPE)&#39;s Code of Conduct for the following policy domains:</p>

<p>&nbsp;</p>

<p><strong>Editorial Principles </strong></p>

<p>&nbsp;</p>

<p><strong><em>1. &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Accountability and responsibility for journal content </strong></em></p>

<p>The editors of the journal are responsible for the policies and procedures in ensuring the quality and integrity of the articles published.</p>

<p><strong><em>2. &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Editorial independence and integrity </em></strong></p>

<p>The responsibility of our editors is to make fair and unbiased decisions in order to uphold the principle of editorial independence and integrity.</p>

<p style="margin-left:36.0pt;"><em>2.1 Separating decision-making from commercial considerations</em></p>

<ol style="list-style-type:lower-alpha;margin-left:100px">
	<li>Our editors make decisions on academic merit and take full responsibility for their decisions.</li>
	<li>Since our journal is not published for commercial purposes (i.e. JEB does not collect any fees from any parties), separating decision-making from commercial considerations is a non-issue.</li>
	<li>Any articles submitted by academician from Universiti Malaysia Kelantan undergo the same rigorous quality control by sending the articles to two external reviewers.</li>
</ol>

<p style="margin-left:36.0pt;"><em>2.2 Editors&rsquo; relationship to the journal publisher or owner </em></p>

<ol style="list-style-type:lower-alpha;margin-left:100px">
	<li>Each editor has an appointment letter setting out the terms and conditions of their appointment with our journal.</li>
	<li>JEB&rsquo;s publisher does not have any role in decisions on content for commercial or political reasons. The publisher also does not dismiss an editor because of any journal content unless there was gross editorial misconduct, or an independent investigation has concluded that the editor&rsquo;s decision to publish was against the journal&rsquo;s scholarly mission.</li>
</ol>

<p style="margin-left:36.0pt;"><em>2.3 Journal metrics and decision-making </em></p>

<ol style="list-style-type:lower-alpha;margin-left:100px">
	<li>The journal does not make any attempt to artificially increase any journal metric or demand that references to that journal&rsquo;s articles are included except for genuine scholarly reasons. In other words, the articles are reviewed on purely scholarly grounds, and that the author(s) is not pressured to cite specific publications for non-scholarly reasons</li>
</ol>

<p>&nbsp;</p>

<p><strong><em>3. &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Editorial confidentiality </em></strong></p>

<p style="margin-left:36.0pt;"><em>3.1 Authors&rsquo; material </em></p>

<ol style="list-style-type:lower-alpha;margin-left:100px">
	<li>The journal protects the confidentiality of the author(s)&rsquo; material and reminds reviewers to do the same and to not share submitted articles with the editors of other journals, unless with the author(s)&rsquo; full agreement or in cases of alleged misconduct.</li>
	<li>Our web-based system does not allow any unauthorised access.</li>
</ol>

<p style="margin-left:36.0pt;">&nbsp;</p>

<p style="margin-left:36.0pt;"><em>3.2 Reviewers </em></p>

<ol style="list-style-type:lower-alpha;margin-left:100px">
	<li>The journal practices a double-blind, peer review process where the author(s) and reviewers do not know each other.</li>
</ol>

<p>&nbsp;</p>

<p><strong><em>4. &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Encourage maximum transparency and complete and honest reporting </em></strong></p>

<p style="margin-left:36.0pt;"><em>4.1 Authorship and responsibility </em></p>

<ol style="list-style-type:lower-alpha;margin-left:100px">
	<li>From the Year 2019, the journal adopts the individual contribution and responsibility in the contributor section.</li>
	<li>The journal does not allow any changes in authorship without prior reasonable justification and consent from all authors.</li>
</ol>

<p style="margin-left:36.0pt;"><em>4.2 Conflicts of interest and role of the funding source </em></p>

<ol style="list-style-type:lower-alpha;margin-left:100px">
	<li>All published articles to include funding sources and a statement from the author(s)&rsquo; addressing any relevant financial and non-financial conflicts of interest.</li>
</ol>

<p style="margin-left:36.0pt;"><em>4.3 Full and honest reporting and adherence to reporting guidelines </em></p>

<ol style="list-style-type:lower-alpha;margin-left:100px">
	<li>Figures, tables and images must be original and adhere to the appropriate standards in the field.</li>
	<li>The journal uses Turnitin software to check similarities of all submitted articles.</li>
</ol>

<p>&nbsp;</p>

<p><strong><em>5. &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Responding to criticisms and concerns </em></strong></p>

<p style="margin-left:36.0pt;"><em>5.1 Ensuring integrity of the published record - corrections </em></p>

<ol style="list-style-type:lower-alpha;margin-left:100px">
	<li>The journal allows the author(s)&rsquo; to retract the articles if the error(s) renders the work or substantial parts of it invalid.</li>
</ol>

<p style="margin-left:36.0pt;"><em>5.2 Ensuring the integrity of the published record &ndash; suspected research or publication misconduct</em></p>

<ol style="list-style-type:lower-alpha;margin-left:100px">
	<li>The Journal follows the <a href="https://publicationethics.org/">Committee on Publication Ethics</a>&nbsp;(COPE)&#39;s procedure to address any issue related to the integrity of research or publication misconduct (<a href="file:///D:/a.%09https/::publicationethics.org:files:Full%20set%20of%20English%20flowcharts_9Nov2016.pdf">Available Online</a>)</li>
</ol>

<p style="margin-left:36.0pt;"><em>5.3 Encourage scholarly debate </em></p>

<ol style="list-style-type:lower-alpha;margin-left:100px">
	<li>The journal provides all author(s)&rsquo; contact details for readers to communicate and to discuss relevant issues directly with researchers.</li>
</ol>

<p>&nbsp;</p>

<p><strong><em>6. &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Ensuring a fair</em></strong><strong><em> and appropriate peer review process </em></strong></p>

<p style="margin-left:36.0pt;"><em>6.1 Decision whether to review </em></p>

<ol style="list-style-type:lower-alpha;margin-left:100px">
	<li>The Managing Editor or Associate Editor checks the similarities index (using Turnitin), the scope of the articles and how it fits with JEB&rsquo;s scope, and sufficient merit for the articles to send to reviewers for further review.</li>
	<li>If the article does not meet the minimum standard(more than 30% and/or the Scope does not fit the journal&rsquo;s and/or overall quality is poor), the article is desk rejected and returned to the author(s) with an explanation of the editors&rsquo; reasoning.&nbsp;</li>
</ol>

<p style="margin-left:36.0pt;"><em>6.2 Interaction with peer reviewers </em></p>

<ol style="list-style-type:lower-alpha;margin-left:100px">
	<li>The review form clearly states that peer reviewers should assess the research and publication ethics issues including if it has any suspicions of plagiarism, fabrication, falsification, or redundant publication.</li>
	<li>The review form includes a declaration of any conflicts of interest from peer reviewers.</li>
	<li>The journal allows the reviewers to complete the review in four weeks. To ensure the timeliness of the review process, at the end of the third week, the associate editor sends a reminder to the reviewers. The editors check the reviewers&rsquo; comments before sending it to the author(s).</li>
</ol>

<p style="margin-left:36.0pt;"><em>6.3 Reviewer Selection Criteria</em></p>

<ol style="list-style-type:lower-alpha;margin-left:100px">
	<li>A reviewer must have a PhD in the relevant field and published at least five articles in a journal listed in WOS/Scopus/ERA in the last 5 years.</li>
	<li>For articles submitted by the author(s) from Universiti Malaysia Kelantan (UMK), the journal appoints two external reviewers. For all other cases, the journal appoints one reviewer from UMK and one reviewer from another Institute or University.</li>
</ol>



<p style="margin-left:36.0pt;"><em>6.4.&nbsp; Reviewer Misconduct </em></p>

<ol style="list-style-type:lower-alpha;margin-left:100px">
	<li>The journal follows the&nbsp;<a href="https://publicationethics.org/">Committee on Publication Ethics</a>&nbsp;(COPE)&#39;s Code of Conduct for reviewer misconduct. (<a href="https://publicationethics.org/files/u2/07_Reviewer_misconduct.pdf">Available Online</a>)</li>
</ol>

<p style="margin-left:36.0pt;">6.5 Interaction with the author(s)</p>

<ol style="list-style-type:lower-alpha;margin-left:100px">
	<li>The journal passes all peer reviewers&rsquo; comments to the author(s) after assessing the validity and appropriateness of the comments. Any libellous or offensive remarks can be excluded.</li>
	<li>Author(s), whose articles have been rejected, may appeal for a review. However, the journal has the right to retain the decision.</li>
</ol>

<p>&nbsp;</p>

<p><strong><em>7. &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Editorial decision-making </em></strong></p>

<p style="margin-left:36.0pt;"><em>7.1 Editorial and journal processes </em></p>

<ol style="list-style-type:lower-alpha;margin-left:100px">
	<li value="1"><strong>Initiall Assessment</strong>: The Managing Editor or an Associate Editor will first evaluate similarities (using Turnitin), the scope of the manuscript (how it fits with JEB&rsquo;s scope), and lastly, whether the manuscript has sufficient merit to send to reviewers for further review. If the manuscript does not reach the minimum standard, the article will be desk rejected and returned to the author(s) with an explanation of the editors&rsquo; reasoning.&nbsp;</li>

	<li value="2"><strong>Peer Review Details: </strong>If a manuscript passes the initial screening, the Managing Editor and/or Associate Editor will recruit reviewers to assess the merits of the manuscript. Upon the receipt of the report from the reviews, the Managing Editor and/or Associate Editor will provide a recommendation to the Editor-in-Chief who will finally decide whether the article should be accepted, whether requiring minor corrections, major correction or rejection.</li>
	<li value="3"><strong>Notification</strong>: E-mail notification will be sent to the corresponding author, announcing that a decision has been made. The possible decisions are Reject, Major Revision, Minor Revision, and Accept.</li>
	<li value="4"><strong>Revisions</strong>: Revisions, if suggested, have to be undertaken within a given time. A manuscript that is not revised within one year may be reclassified as &quot;withdrawn,&quot; and revisions submitted after the one-year mark may be treated as new submissions. All revisions should be accompanied by a separate &quot;Response to Reviewers&quot; document, which provides a summary of the major changes made to the document and a point-by-point description of whether and how the issues raised by the reviewers and editors have been addressed.</li>
	<li value="5"><strong>Rejection</strong>: As a rule, JEB does not invite resubmissions of rejected manuscripts. If a submitted manuscript is based on an article previously rejected by the Journal, it is the responsibility of the author(s) to reveal this and to state clearly why resubmission is justified. Failure to do this may be grounds for rejection.</li>
	<li value="6"><strong>After Acceptance - Final Submission</strong>: If the article is conditionally accepted for publication, the corresponding author(s) will be requested to submit the final version of the manuscript using the JEB Template.</li>
</ol>

<p style="margin-left:36.0pt;"><em>7.2 Editorial conflicts of interest </em></p>

<ol style="list-style-type:lower-alpha;margin-left:100px">
	<li>The editors and the author(s) from Universiti Malaysia Kelantan are not involved in the decision about the articles submitted.&nbsp;</li>
	<li>Any articles submitted by editors and author(s) from Universiti Malaysia Kelantan undergo the same rigorous quality control by sending the articles to two external reviewers.</li>
</ol>
			
